package accessSpecifiers.protectedas;

public class Shape {

	protected int length;
	protected int breadth;
	
	public int getLength() {
		return length;
	}
	public int getBreadth() {
		return breadth;
	}
	public void setData(int length, int breadth) {
		this.breadth = breadth;
		this.length = length;

	}
	
	
}
