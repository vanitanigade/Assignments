package control_statement_1002;

public class NestedIf {

	public static void main(String[] args) { 
		int x = 10; 
		int y = 12;      

		if( x == 10 ){
			if( y == 10 ){ 
				System.out.print("Both variables are equal"); 
			} else  { 
				System.out.println("Both are not equal");        
			} 
		} 
	}


}
