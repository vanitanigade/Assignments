package control_statement_1002;

public class PrePostIncrement {

	public static void main(String args[]){ 
		
		int i=50;   
		
		System.out.println("The value is "+i +".");   

		System.out.println("The value of i++ is "+ i++ +".");   

		System.out.println("The value of ++i is "+ ++i +".");   

		System.out.println("The value of ++i is "+ ++i +".");         
	} 


}

