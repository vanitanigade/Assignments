package control_statement_1002;

public class WhileLoop {

	public static void main(String[] args) {
		int i = 0;    //initialization  	    
		while(i < 5)     //condition to be check  
		{  
			System.out.println("i is : " + i);  	      
			i++;              							//update statment 
		} 
	}  


}
